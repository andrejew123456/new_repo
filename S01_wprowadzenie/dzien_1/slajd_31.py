### Podstawowe operacje na listach ###

#  + i *
[1, 2 , 3] + [4, 5, 6] >> [1, 2, 3, 4, 5, 6]
[1, 2, 3] * 3  >>  [1, 2, 3, 1, 2, 3, 1, 2, 3]

#slice
L = ['mielonka', "Mielonka", "MIELONKA!"]
L[2]   >>  'MIELONKA!'
L[-2]  >>  'Mielonka'
L[1:]  >>  ['Mielonka', 'MIELONKA!']

#macierze
matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
matrix[1]  >> [4, 5, 6]
matrix[1][1] >> 5


